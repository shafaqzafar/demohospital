import { Schema, model, models } from 'mongoose'

const UserSchema = new Schema({
  username: { type: String, required: true, unique: true },
  role: { type: String, enum: ['admin','technician','reception'], default: 'technician' },
  passwordHash: { type: String, required: true },
}, { timestamps: true })

export type DiagnosticUserDoc = {
  _id: string
  username: string
  role: 'admin'|'technician'|'reception'
  passwordHash: string
}

export const DiagnosticUser = models.Diagnostic_User || model('Diagnostic_User', UserSchema)
